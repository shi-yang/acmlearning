#include <iostream>
#include <cstdio>
#include <queue>

using namespace std;

int hx, hy, sx, sy;
int main()
{
	scanf("%d %d %d %d", &hx, &hy, &sx, &sy);
	int p, q;
	while (~scanf("%d %d", &p, &q)) {
		
		for (;;) {
			scanf("%d %d", &p, &q);
			if (p == -1 && q == -1)
				break;

		}
	}
	return 0;
}
